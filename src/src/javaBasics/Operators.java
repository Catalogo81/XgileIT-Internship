package javaBasics;

public class Operators
{
    public static void main(String[] args) {

        //This is a program that shows all the operator one can use in Java

        int addition, subtraction, multiplication, division, modulus;

        /* addition */
        addition = 1 + 4;
        System.out.println("1 + 4 = " + addition);

        /* subtraction */
        subtraction = 7 - 3;
        System.out.println("7 - 3 = " + subtraction);

        /* multiplication */
        multiplication = 6 * -10;
        System.out.println("6 * -10 = " + multiplication);

        /* division */
        division = 8 / 4;
        System.out.println("8 / 4 = " + division);

        /* modulus */
        modulus = 145 % 10;
        System.out.println("145 % 10 = " + modulus);

    }
}
