package loops;

/**
 * Java Program of a For Loop demonstration
 * the syntax
 * and how to use a For Loop
 *
 * @author Kgotso Matjato
 */
public class ForLoop {

    public static void main(String[] args)
    {
        /*
         Syntax:
         for(initialization; condition; increment or decrement statement)
            {
                code..
            }
         */

        for(int i = 1; i <= 3; i++)
        {
            System.out.println("the for loop is running");
        }

        System.out.println("the for loop ended");
    }
}
